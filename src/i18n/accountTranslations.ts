export function useAccountT() {
  const { language } = useLanguage();
  return {
    t: dicts[language],
    locale: languageToLocale[language],
    formatDate: (iso: string) =>
      new Date(iso).toLocaleDateString(languageToLocale[language], {
        day: "2-digit",
        month: "short",
        year: "numeric",
      }),
  };
}

/** Non-hook resolver for places that don't have a component context. */
export function getAccountDict(language: Language): AccountDict {
  return dicts[language];
}
